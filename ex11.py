#number data types:
#integer
#in python any whole number will be treated as an integer
a=10
print(a)
print(type(a))

#float
#in python any decimal number will be treated as float value
b=10.3
print(b)
print(type(b))

#complex numbers:
#the combination of real number and an imaginary number
c=5+6j
print(c)
print(type(c))

#binary numbers:
#they starts with 0b
#base value is 2
#the possible values are 0,1
a=4
print(bin(a))

b=0b1010
print(b)

#hexa decimal numbers:
#starts with 0x
#base value is 16
#possible values are 0-9 and a-f

c=10
print(hex(c))

d=0xa
print(d)

#octal numbers
#starts with 0o
#base value is 8
#possible values are 0-7
d=10
print(oct(d))

e=0o12
print(e)