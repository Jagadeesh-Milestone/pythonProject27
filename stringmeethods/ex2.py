#strip
#it is used to remove whitespaces at beginning or ending
a=" hello world "
print(len(a))
print(a)
print(a.strip())
print(a.lstrip())
print(a.rstrip())

#replace:
#it is used to replace the character
b='Hello Python learners'
print(b.replace('l','apple'))
print(b.replace('l','mango',2))

#find:
#it is used to find the position of a character
c='Hello bangalore'
print(c.find('l'))
print(c.rfind('l'))

