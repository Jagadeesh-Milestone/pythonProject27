#arithemetic operators:
a=10
b=5
print(a+b)#addition
print(a-b)#subraction
print(a*b)#multiplication
print(a/b)#division --quotient
print(a%b)#modulus  --reminder value
print(a**b)#power  --a power b
print(a//b)#floor division

#assignment operators:
x=20
x+=3
print(x)

y=30
y-=4
print(y)

z=5
z*=3
print(z)

#logical operators:
#logical and
#it returns true if both the statements are true:
print(True and True)
print(True and False)
print(False and True)
print(False and False)

#logical or
#it returns true if any one of the statement is true
print(True or True)
print(True or False)
print(False or True)
print(False or False)

#logical not
#it returns opposite to the given statement:
print(not False)
print(not True)
