#for:
#for loop is used to loop over the sequences:
x=[10,20,30,40]
for i in x:
    print(i)

y='bangalore'
for a in y:
    print(a)

#break:
a=[10,20,30,40,40,50,60]
for i in a:
    print(i)
    if i==40:
        break

#continue:
b="hyderabad"
for i in b:
    if i=='r':
        continue
    print(i)

